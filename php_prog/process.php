                Higher Education Online Admissions and Enrolment Procedure for 1st Semester SY 2022-2023 
For New Freshmen and Transferees under Waived Entrance Examination

 <br>

<strong>A. ADMISSIONS & ENROLMENT</strong>

 <br>
 <br>
<strong>Step 1.   </strong>     
<br>

Undergo the PRE-SCREENING REGISTRATION by accessing the link to the school/college you are applying for. Refer to the LIST of programs offered under waived entrance exam admissions to be guided on which school/college your chosen program belongs.

Prepare clear photo or scanned copy of the following documents:
✓ Grade 12 Report Card (Form 138) with remarks eligible for college admissions and signed by the School Principal (For Grade 12 Graduates), or,
✓ Transcript of Records & Good Moral Certificate (For Transferees)

 <br>
<br>
<li>College of Agriculture Pre-Screening Registration</li>
<li>College of Arts and Sciences Pre-Screening Registration</li>
<li>College of Computer Studies Pre-Screening Registration</li>
<li>College of Engineering  Pre-Screening Registration</li>
<li>School of Business and Mgmt. Pre-Screening Registration</li>

 <br>
 <br>
<strong>Step 2.      </strong>
<br>
Applicant will be notified via e-mail of the APPROVED PROGRAM together with a unique REGISTRATION NUMBER and log in credentials. The registration number will be used to create an application in the admissions portal: http://apply.xu.edu.ph/
<br>
After successful processing of application in the admissions portal, an ADMISSIONS CLEARANCE will be sent to the applicant’s registered email. The Admissions Clearance will be used to proceed with the online enrolment registration.

 <br>
 <br>
<strong>Step 3.      </strong>
<br>
Pay the required down payment of Php3,000.00 
Go to this link https://www.xu.edu.ph/payment-centers-finance to view the list of accredited XU payment centers.
<br>

IMPORTANT: Secure ADMISSIONS CLEARANCE prior to making a down payment. Carefully follow the instructions in filling out the bank forms as indicated on the Admissions Clearance.
 <br>
 <br>
<strong>Step 4.      </strong>
<br>
Process Online Enrolment Registration for school/college by accessing this link https://www.xu.edu.ph/admissions-enrollment
<br>
 <br>

Prepare clear photos or scanned copies of the following documents:
<br>
<li> a) Admissions Clearance</li>
<li>b) Grade 12 Report Card (Form 138) for Freshmen/ Transfer Credentials (formerly called Honorable Dismissal) for Transferees</li>
<li>c) PSA Birth Certificate</li>
<li>d) Validated Down Payment Deposit Slip (or Notice of Scholarship Award for scholars)</li>
<li>e) Signed Data Privacy Consent Form (downloadable from the enrolment registration form)</li>
<br>
After completing the online registration, a notification will be emailed to the applicant regarding the status of their application for enrollment in XU.
If successful, they will be given their Login credentials to access the Student Lifecycle Management and Information System (SLMIS) to view their class schedule and other related information.
 <br>

