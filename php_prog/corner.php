ior High School?</h3><p>Xavier University has been preparing for this transition since 2014. Its Center for Integrated Technologies (CIT) has already received approval to offer the Tech-Voc track in SY 2014-15.&nbsp; CIT has partnerships with BMW, Toyota, General Motors and local companies to employ its graduates.</p><p>The main campus along Corrales Avenue, including all its improved facilities and resources, will cater to the educational needs of Senior High School students. Beginning SY 2016-17, its existing dynamic faculty, considered experts and experienced in their fields, will be tapped for the new Senior High School programs.</p><h3>Contact Information</h3><p>(088) 853 -&nbsp;9702 - Secretary, Office of the Principal</p><p>(088) 853 -&nbsp;9703 -&nbsp;Secretary, Office of the Asst Principal for Academics</p><p>(088) 853 -&nbsp;9693 -&nbsp;Secretary, Office of the Asst Principal for Administration</p></div>
